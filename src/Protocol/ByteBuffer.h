#ifndef _BYTE_BUFFER	
#define _BYTE_BUFFER

#pragma warning(disable: 4244)

////////////////////////////////////////////////////////////
// Includes
////////////////////////////////////////////////////////////
#include <stdint.h>
#include <windows.h>

////////////////////////////////////////////////////////////
// Max buffer size (10Kbs)
////////////////////////////////////////////////////////////
#define MAX_BUFFER_SIZE     10240

////////////////////////////////////////////////////////////
// Size defines
////////////////////////////////////////////////////////////
#define BYTE_MEMORY_SIZE    1
#define CHAR_MEMORY_SIZE    1
#define INTEGER_MEMORY_SIZE 2
#define LONG_MEMORY_SIZE    4
#define FLOAT_MEMORY_SIZE   4
#define DOUBLE_MEMORY_SIZE  8

////////////////////////////////////////////////////////////
// Buffer struct
////////////////////////////////////////////////////////////
struct Buffer
{
	uint8_t byteArray[MAX_BUFFER_SIZE];
	int     writePos;
	int     readPos;
	int     BufferSize;
};

////////////////////////////////////////////////////////////
// Copy byte in a buffer
////////////////////////////////////////////////////////////
void buffer_Copy(struct Buffer * tBuffer, void * Data, int sizeGet);

////////////////////////////////////////////////////////////
// Get byte of the buffer
////////////////////////////////////////////////////////////
void * buffer_Get(struct Buffer * tBuffer, int sizeGet);

////////////////////////////////////////////////////////////
// Put int
////////////////////////////////////////////////////////////
void buffer_PutInt(struct Buffer * tBuffer, int Val);

////////////////////////////////////////////////////////////
// Put long
////////////////////////////////////////////////////////////
void buffer_PutLong(struct Buffer * tBuffer, long Val);

////////////////////////////////////////////////////////////
// Put byte
////////////////////////////////////////////////////////////
void buffer_PutByte(struct Buffer * tBuffer, int Value);

////////////////////////////////////////////////////////////
// Put float
////////////////////////////////////////////////////////////
void buffer_PutFloat(struct Buffer * tBuffer, float Value);

////////////////////////////////////////////////////////////
// Put double
////////////////////////////////////////////////////////////
void buffer_PutDouble(struct Buffer * tBuffer, double Value);

////////////////////////////////////////////////////////////
// Put string
////////////////////////////////////////////////////////////
void buffer_PutString(struct Buffer * tBuffer, char * str);

////////////////////////////////////////////////////////////
// Get int
////////////////////////////////////////////////////////////
int buffer_GetInt(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get long
////////////////////////////////////////////////////////////
long buffer_GetLong(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get byte
////////////////////////////////////////////////////////////
int buffer_GetByte(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get float
////////////////////////////////////////////////////////////
float buffer_GetFloat(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get double
////////////////////////////////////////////////////////////
double buffer_GetDouble(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get string
////////////////////////////////////////////////////////////
char * buffer_GetString(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Clean buffer
////////////////////////////////////////////////////////////
void buffer_Clear(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Read position
////////////////////////////////////////////////////////////
int buffer_ReadPos(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Write position
////////////////////////////////////////////////////////////
int buffer_WritePos(struct Buffer * tBuffer);

////////////////////////////////////////////////////////////
// Get buffer capacity
////////////////////////////////////////////////////////////
int buffer_Capacity();

////////////////////////////////////////////////////////////
// Set buffer size
////////////////////////////////////////////////////////////
void buffer_SetSize(struct Buffer * tBuffer, int Size);

#endif